    //count down timer.idea was taken from youtube video.
    public void timer(){
    
        java.util.Timer time = new java.util.Timer();
        counter = 10;
        
        TimerTask task = new TimerTask(){  //task object
        
            @Override
            public void run(){
            
                jLabel11.setText(Integer.toString(counter));
                counter --;
                
                if(counter == -1){
                
                    time.cancel();
                    JOptionPane.showMessageDialog(rootPane, "Game Over!");
                    MainMenuSwtich();
                } 
                else if(isIt){
                    time.cancel();
                    isIt = false;
                }
            }
        };
        time.scheduleAtFixedRate(task, 1000, 1000);
    }
